create table if not exists public.catalog_agent_lexical_candidate_overrides (
  id bigint generated by default as identity primary key,
  source_term text not null,
  candidate_term text not null,
  expansion_source text not null default 'governed_override',
  notes text,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint catalog_agent_lexical_candidate_overrides_terms_not_blank
    check (length(btrim(source_term)) > 0 and length(btrim(candidate_term)) > 0),
  constraint catalog_agent_lexical_candidate_overrides_unique_active
    unique (source_term, candidate_term, expansion_source)
);

