-- Phase A: unified ingredient catalog foundation (non-destructive).
-- Creates canonical ingredient tables, backfills from current local produce/basic datasets,
-- and adds legacy compatibility mapping + read-only summary view.

create extension if not exists pgcrypto;

create table if not exists public.ingredients (
  id uuid primary key default gen_random_uuid(),
  slug text not null unique,
  ingredient_type text not null check (ingredient_type in ('produce', 'basic')),
  is_seasonal boolean not null default false,
  season_months int[] not null default '{}',
  default_unit text not null default 'piece',
  supported_units text[] not null default '{}',
  grams_per_unit jsonb not null default '{}'::jsonb,
  ml_per_unit jsonb not null default '{}'::jsonb,
  grams_per_ml double precision,
  calories_per_100g double precision,
  protein_per_100g double precision,
  carbs_per_100g double precision,
  fat_per_100g double precision,
  fiber_per_100g double precision,
  vitamin_c_per_100g double precision,
  potassium_per_100g double precision,
  quality_status text not null default 'active',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.ingredient_localizations (
  ingredient_id uuid not null references public.ingredients(id) on delete cascade,
  language_code text not null,
  display_name text not null,
  short_name text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (ingredient_id, language_code)
);

create table if not exists public.ingredient_aliases_v2 (
  id bigint generated by default as identity primary key,
  ingredient_id uuid not null references public.ingredients(id) on delete cascade,
  alias_text text not null,
  normalized_alias_text text not null,
  language_code text,
  source text not null default 'manual',
  confidence double precision check (confidence is null or (confidence >= 0 and confidence <= 1)),
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists ingredient_aliases_v2_unique_active_normalized_alias_idx
  on public.ingredient_aliases_v2(normalized_alias_text)
  where is_active = true;

create table if not exists public.legacy_ingredient_mapping (
  ingredient_id uuid not null references public.ingredients(id) on delete cascade,
  legacy_produce_id text,
  legacy_basic_id text,
  source_domain text not null default 'phase_a_seed',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint legacy_ingredient_mapping_one_legacy_id_check
    check (
      ((case when legacy_produce_id is null then 0 else 1 end) +
       (case when legacy_basic_id is null then 0 else 1 end)) = 1
    )
);

create unique index if not exists legacy_ingredient_mapping_produce_unique_idx
  on public.legacy_ingredient_mapping(legacy_produce_id)
  where legacy_produce_id is not null;

create unique index if not exists legacy_ingredient_mapping_basic_unique_idx
  on public.legacy_ingredient_mapping(legacy_basic_id)
  where legacy_basic_id is not null;

create unique index if not exists legacy_ingredient_mapping_ingredient_id_unique_idx
  on public.legacy_ingredient_mapping(ingredient_id);

