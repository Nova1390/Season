-- Bridge old split alias model into canonical alias v2 when legacy table exists.
do $$
begin
  if to_regclass('public.ingredient_aliases') is not null then
    insert into public.ingredient_aliases_v2 (
      ingredient_id,
      alias_text,
      normalized_alias_text,
      language_code,
      source,
      confidence,
      is_active,
      updated_at
    )
    select
      m.ingredient_id,
      a.alias_text,
      a.normalized_alias_text,
      a.language_code,
      coalesce(a.source, 'manual'),
      a.confidence,
      coalesce(a.is_active, true),
      now()
    from public.ingredient_aliases a
    join public.legacy_ingredient_mapping m
      on (a.produce_id is not null and m.legacy_produce_id = a.produce_id)
      or (a.basic_ingredient_id is not null and m.legacy_basic_id = a.basic_ingredient_id)
    on conflict do nothing;
  end if;
end
$$;

create or replace view public.ingredient_catalog_summary as
select
  i.id as ingredient_id,
  i.slug,
  i.ingredient_type,
  max(case when l.language_code = 'en' then l.display_name end) as en_name,
  max(case when l.language_code = 'it' then l.display_name end) as it_name,
  m.legacy_produce_id,
  m.legacy_basic_id
from public.ingredients i
left join public.ingredient_localizations l
  on l.ingredient_id = i.id
left join public.legacy_ingredient_mapping m
  on m.ingredient_id = i.id
group by i.id, i.slug, i.ingredient_type, m.legacy_produce_id, m.legacy_basic_id;

